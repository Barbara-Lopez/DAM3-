public class cestos {
    private int mangas;
    private int cuerpos;


    public cestos(int mangas, int cuerpos) {
        this.mangas = mangas;
        this.cuerpos = cuerpos;
    }

    public int getMangas() {
        return mangas;
    }

    public void setMangas(int mangas) {
        this.mangas = mangas;
    }

    public void setCuerpos(int cuerpos) {
        this.cuerpos = cuerpos;
    }

    public int getCuerpos() {
        return cuerpos;
    }
}
